public class NotificationService {
    public void notifyAcceptance(){
        System.out.println("NOTIFICATION: SUBMISSION FULLY ACCEPTED!");
    }
    
    public void notifyRejection(){
        System.out.println("NOTIFICATION: SUBMISSION REJECT!");
    }
    
    public void notifyRevision(){
        System.out.println("NOTIFICATION: REVISION REQUIRED!");
    }
}
